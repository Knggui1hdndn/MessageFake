package com.example.messagefake;

public class detail {
    private int img;
    private String txt;

    public detail(int img, String txt) {
        this.img = img;
        this.txt = txt;
    }

    public detail() {
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }
}
